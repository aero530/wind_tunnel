#ifndef _MAIN_H_
#define _MAIN_H_

#include "usb_protocol_cdc.h"




// The input commands are never going to be
// more than 14 chars long. Volatile for the ISR.
volatile unsigned char data_in[30]; //!< Volatile char array to store string input on USB->USART temporarily before it is copied to command_in for processing
// if changing the size of this array also change memory copy size in usb_cdc_interface.c copy_command()
volatile unsigned char command_in[30]; //!< Volatile char array used to process a string command from USB->USART connection

volatile unsigned char data_count; //!< Volatile char to keep track of location in data_in array during command line input
volatile unsigned char command_ready; //!< Volatile char to keep track of when a command is ready to be processed (checked in main while loop of program)

//! \brief Process the command input over USB -> USART
void process_command(void);

void process_message(void);

void update_time(void);

void update_pwm(void);

void update_tc(void);

static void pwm_ovf_interrupt_callback(void);


/*! \brief Opens the communication port
 * This is called by CDC interface when USB Host enable it.
 *
 * \retval true if cdc startup is successfully done
 */
bool main_cdc_enable(uint8_t port);

/*! \brief Closes the communication port
 * This is called by CDC interface when USB Host disable it.
 */
void main_cdc_disable(uint8_t port);

/*! \brief Manages the leds behaviors
 * Called when a start of frame is received on USB line each 1ms.
 */
void main_sof_action(void);

/*! \brief Enters the application in low power mode
 * Callback called when USB host sets USB line in suspend state
 */
void main_suspend_action(void);

/*! \brief Turn on a led to notify active mode
 * Called when the USB line is resumed from the suspend state
 */
void main_resume_action(void);

/*! \brief Save new DTR state to change led behavior.
 * The DTR notify that the terminal have open or close the communication port.
 */
void main_cdc_set_dtr(uint8_t port, bool b_enable);

#endif // _MAIN_H_
